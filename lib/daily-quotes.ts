/**
 * 멤버ID + 날짜 기반 고정 랜덤 격언 반환
 * - 같은 멤버가 같은 날 새로고침해도 동일 격언
 * - 연도가 달라도 같은 날짜에 다른 격언 (연도 포함 해싱)
 * - 멤버마다 다른 격언
 */
export function getDailyQuote(memberId: string): string {
    const today = new Date();
    const seed = `${memberId}-${today.getFullYear()}-${today.getMonth()}-${today.getDate()}`;

    // djb2 해시 — 충분히 분산되는 간단한 해시
    let hash = 5381;
    for (let i = 0; i < seed.length; i++) {
        hash = ((hash << 5) + hash) + seed.charCodeAt(i);
        hash |= 0;
    }

    const index = Math.abs(hash) % dailyQuotes.length;
    return dailyQuotes[index];
}
